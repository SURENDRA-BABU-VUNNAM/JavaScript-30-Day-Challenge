// Write a script that performs basic arithmetic operations (addition, subtraction, multiplication, division, remainder) on two numbers and logs the results.

let num_1 = 12321;
let num_2 = 121;

// addition

let add = num_1 + num_2;
console.log(add) // output: 12442

// subtraction
let sub = num_1 - num_2;
console.log(sub) // output: 12200

// multiplication
let mul = num_1 * num_2;
console.log(mul) // output: 146400

// division
let div = num_1 / num_2;
console.log(div) // output: 101.82644628099173

// remainder
let rem = num_1 % num_2;
console.log(rem) // output: 100

